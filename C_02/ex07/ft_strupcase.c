char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	if (str[0] == '\0')
	{
		return (str);
	}
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] - 32;
		}
		else
		{
			i++;
		}
	}
	return (str);
}
