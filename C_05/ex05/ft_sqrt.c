int	ft_sqrt(int nb)
{
	int	s;

	s = 2;
	if (nb == 1)
	{
		return (1);
	}
	while (s <= nb / s)
	{
		if ((s * s) == nb)
		{
			return (s);
		}
		s++;
	}
	return (0);
}
